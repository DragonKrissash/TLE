void solve(){
    int n;cin>>n;
    vi v(n);
    input(v);
    bool y=1;
    for(int a=0;a<n;a++){
        if(v[a]!=0){
            y=0;
            break;
        }
    }
    if(y){
        cout<<1<<nl;
        return;
    }
    vector<vi>arr(31);
    for(int a=0;a<n;a++){
        for(int b=0;b<31;b++){
            if(v[a]&(1<<b)){
                arr[b].push_back(a);
            }
        }
    }
    int ans=0;
    for(int a=0;a<31;a++){
        if(arr[a].size()==0)continue;
        ans=max(arr[a][0]+1,ans);
        for(int b=1;b<arr[a].size();b++)
        ans=max(ans,arr[a][b]-arr[a][b-1]);
        ans=max(ans,n-arr[a].back());
    }
    cout<<ans<<nl;
}



