void solve(){
    int n;cin>>n;
    vi v(n),pos(n+1),pre(n);
    input(v);
    string s;cin>>s;
    for(int a=0;a<n;a++){
        pos[v[a]]=a;
    }
    pre[0]=s[0]-'0';
    for(int a=1;a<n-1;a++){
        pre[a]=pre[a-1]+(s[a]-'0');
    }
    pre[n-1]=pre[n-2];
    for(int a=1;a<n;a++){
        if(pos[a+1]<pos[a]){
            int r=pos[a];
            int l=pos[a+1];
            int sum=pre[r-1]-(l-1>=0 ? pre[l-1]:0);
            if(sum<r-l){
                no;
                return;
            }
        }
    }
    yes;
}



