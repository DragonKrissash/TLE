bool check(int w,int c,vi &v){
    int n=v.size();
    int sum=0;
    for(int a=0;a<n;a++){
        int temp=(v[a]+2*w)*(v[a]+2*w);
        sum=sum+temp;
        if(sum>c)
        return sum<=c;
    }
    return sum<=c;
}

void solve(){
    int n,c;cin>>n>>c;
    vi v(n);
    input(v);
    int l=0,r=1e9;
    int ans=0;
    while(l<=r){
        int m=l+(r-l)/2;
        if(check(m,c,v)){
            ans=m;
            l=m+1;
        }
        else r=m-1;
    }
    cout<<ans<<nl;
}



