void solve(){
    int n,w;cin>>n>>w;
    vi wei(n),co(n);
    input(wei);
    input(co);
    int sum=0,cost=0,ans=0;
    for(int l=0,r=0;r<n;){
        sum+=wei[r];
        cost+=co[r];
        while(sum>w){
            sum-=wei[l];
            cost-=co[l];
            l++;
        }
        ans=max(ans,cost);
        r++;
    }
    cout<<ans<<nl;
}



